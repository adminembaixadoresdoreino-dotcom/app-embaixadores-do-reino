package br.com.emanuel.embaixadores_backend.enums;

/**
 * TipoAula — Tipos de aula disponíveis no sistema de formação.
 *
 * - VIDEO_AULA: aula gravada com vídeo (o tipo mais comum)
 * - ATO_PROFETICO: aula ao vivo especial ("Atos Proféticos")
 * - LEITURA: conteúdo em texto para leitura
 */
public enum TipoAula {
    VIDEO_AULA,     // Aula gravada com vídeo
    ATO_PROFETICO,  // Aula ao vivo especial
    LEITURA         // Conteúdo em texto
}
